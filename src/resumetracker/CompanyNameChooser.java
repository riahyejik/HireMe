    private void jButton1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseReleased
        // TODO add your handling code here:
        if (SelectedCompanyField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please select company");
        } else {
            ResumeTracker.UpdateCompanyField.setText(SelectedCompanyField.getText());
            ResumeTracker.getUpdateAppData();
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton1MouseReleased

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        SelectedCompanyField.setText((String) jTable1.getValueAt(index, 0));
    }//GEN-LAST:event_jTable1MouseClicked

    private void SearchCompanyFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchCompanyFieldKeyReleased
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<> (model);
        jTable1.setRowSorter(sorter);
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + SearchCompanyField.getText()));
        if (SearchCompanyField.getText().equals("")) {
            showCompanyList();
        }
    }//GEN-LAST:event_SearchCompanyFieldKeyReleased

