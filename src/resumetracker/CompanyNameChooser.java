//    Show list of companies that I applied
    public void showCompanyList() {
        Connection con = MySqlConnection.getConnection();
        PreparedStatement ps;
        String query = "SELECT Company FROM applied";
        Statement st;
        ResultSet rs;
        
        try {
            st = con.createStatement();
            rs = st.executeQuery(query);
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] row = new Object[1];
            
            while (rs.next()) {
                row[0] = rs.getString("Company");
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CompanyNameChooser.class.getName()).log(Level.SEVERE, null, ex);
        }         
    }

